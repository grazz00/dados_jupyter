##operadores aritmeticos
#soma (+)
print("2 + 2 =", 2+2)

#subtacao (-)
print("3 - 2 =", 3-2)

#multiplicacao (*)
print("4 x 2 =", 4*4)

#divisao (/)
print("7 / 2 =", 7/2)

#divisao inteira(//) -> retorna apenas a parte inteira da divisao 
print("7 // 2 =", 7//2)

#modulo(%) -> olha somente o resto da divisao 
#exemplo: 3//2 é igual a 1, para o resto sobram 1
print("7 % 2 =", 7%2)

##operadores relacionais --> o resultado sempre vai ser True ou False
#menor que (<)
print("7 < 2 ?", 7<2) #7 é menor que 2? 

#maior que (>)
print("7 > 2 ?", 7>2) #7 é maior que 2? 

#menor ou igual (<=)
print("8 <= 8", 8<=8) #8 é menor ou igual a 8?

#maior ou igual (>=)
print("10 >= 15", 10>=15) #10 é maior ou igual a 15? 

#igualdade(==)
print("10 == 20", 10==20) #10 é igual a 20? 

#diferente(!=) 
print("5 != 20", 5!=20) #5 é diferente de 20? 

